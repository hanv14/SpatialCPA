"""SpatialCPA-v14 (H3D-FLA — flow matching in a joint latent space) — benchmark-pbya-v2 wrapper.

v14 implements the ``H3D_FLA_Pipeline.md`` proposal: a **conditional flow-matching** model
in a **joint molecular-morphological latent space**, conditioned on a **3D
positional-attention context** aggregated over the real slices, trained **gap-aware**
(whole context slices randomly dropped) with **z-marginalization** and
**biology-informed regularizers** (interface coherence, adaptive smoothness,
hypoxia-gradient directionality, closed-loop consistency). Virtual slices are generated by
integrating the conditional ODE from noise, decoding the joint latents into expression +
type, and grounding each cell in the nearest real training profile. Runs in
``bench_spatialcpa`` (PyTorch >= 2.0); falls back to a latent-grounded recombination if
torch is unavailable. See ``spatialcpav14/README.md``.

Every default in ``SpatialCPAv14Config`` is the intended production setting, so running
this wrapper with only the shared generation-only arguments reproduces the proposed
pipeline; the extra CLI flags below are for ablations / tuning only.
"""

import argparse
import os
import sys
import time
from pathlib import Path

import anndata as ad
import numpy as np
import scipy.sparse as sp

_HERE = Path(__file__).resolve().parent
sys.path.insert(0, str(_HERE))
sys.path.insert(0, str(_HERE.parent))
import _v2_io  # noqa: E402
import leakage_guard  # noqa: E402


def _add_root():
    for cand in ([Path(os.environ["SPATIALCPAV14_ROOT"])] if os.environ.get("SPATIALCPAV14_ROOT") else []) \
            + list(Path(__file__).resolve().parents):
        if (cand / "spatialcpav14" / "__init__.py").exists():
            if str(cand) not in sys.path:
                sys.path.insert(0, str(cand))
            return str(cand)
    return None


_ROOT = _add_root()


def check_environment():
    if _ROOT is None:
        print("ERROR: could not locate the `spatialcpav14` package.", file=sys.stderr)
        return False
    try:
        import spatialcpav14
        from spatialcpav14 import SpatialCPAv14, SpatialCPAv14Config  # noqa: F401
        try:
            import torch
            tinfo = f"torch {torch.__version__} ({'cuda' if torch.cuda.is_available() else 'cpu'})"
        except Exception:
            tinfo = "torch UNAVAILABLE -> latent-grounded fallback"
        print(f"spatialcpav14 v{getattr(spatialcpav14, '__version__', '?')} from {_ROOT}; {tinfo}")
        return True
    except ImportError as e:
        print(f"ERROR: failed to import spatialcpav14: {e}", file=sys.stderr)
        return False


def _to_dense_f32(X):
    return np.asarray(X.toarray() if sp.issparse(X) else X, dtype=np.float32)


def _normalize_expression(adata):
    import scanpy as sc
    et = adata.uns.get("expression_type", "raw_counts")
    if et == "raw_counts":
        sc.pp.normalize_total(adata, target_sum=1e4); sc.pp.log1p(adata)
    elif et in ("log1p_normalized", "log2_normalized", "normalized"):
        pass
    elif et in ("fluorescence_intensity", "mean_intensity"):
        sc.pp.log1p(adata)
    else:
        sc.pp.normalize_total(adata, target_sum=1e4); sc.pp.log1p(adata)
    return et


def build_stack(adata, ct_all):
    from spatialcpav14 import Slice, SliceStack
    sections = adata.obs["section"].values.astype(str)
    coords = np.asarray(adata.obsm["spatial"], dtype=np.float32)
    X = _to_dense_f32(adata.X)
    labels = sorted(np.unique(sections), key=lambda s: np.median(coords[sections == s, 2]))
    slices = [Slice(expression=X[sections == sec], coords_xy=coords[sections == sec, :2],
                    z_values=coords[sections == sec, 2],
                    cell_type_indices=ct_all[sections == sec] if ct_all is not None else None,
                    section_id=str(sec)) for sec in labels]
    return SliceStack(slices)


def run_method(adata, targets, gene_names, args):
    from spatialcpav14 import SpatialCPAv14, SpatialCPAv14Config

    train_mask = np.ones(adata.n_obs, dtype=bool)
    ct_all, cell_type_names = leakage_guard.build_labels_train_only(
        adata, "cell_type", train_mask, seed=args.seed)
    print(f"  cell types: {None if cell_type_names is None else len(cell_type_names)}")

    stack = build_stack(adata, ct_all)
    if sum(s.n_spots for s in stack.slices) < 8 or stack.n_slices < 2:
        print("  SKIP: need >=2 sections and >=8 cells")
        return {}

    cfg = SpatialCPAv14Config()
    cfg.seed = args.seed
    cfg.train.seed = args.seed
    cfg.train.epochs = args.epochs
    cfg.train.pretrain_epochs = args.pretrain_epochs
    cfg.train.device = args.device
    cfg.latent.expr_latent_dim = args.latent_dim
    cfg.encoder.joint_dim = args.joint_dim
    cfg.flow.n_ode_steps = args.ode_steps
    cfg.flow.n_ensemble = args.ensemble
    cfg.generation.position_mode = args.position_mode
    cfg.generation.displacement_scale = args.displacement_scale
    cfg.generation.ground_blend_flow = args.ground_blend_flow
    cfg.generation.edit_weight = args.edit_weight
    cfg.generation.ground_expression = not args.no_ground
    cfg.generation.output_counts = not args.no_output_counts
    cfg.generation.composition_match = not args.no_composition_match
    if args.no_bio:
        cfg.bio.w_interface = cfg.bio.w_hypoxia = cfg.bio.w_consistency = cfg.bio.w_smooth = 0.0
    if args.no_attention:
        cfg.attn.n_context = 1
        cfg.attn.n_global_tokens = 0

    print(f"  epochs(A={cfg.train.pretrain_epochs},B={cfg.train.epochs}), "
          f"latent(d_e={cfg.latent.expr_latent_dim},h={cfg.encoder.joint_dim}), "
          f"flow(steps={cfg.flow.n_ode_steps},ens={cfg.flow.n_ensemble}), "
          f"pos={cfg.generation.position_mode}, bio={not args.no_bio}, "
          f"ground={cfg.generation.ground_expression}")

    gen = SpatialCPAv14(stack, gene_names=gene_names, cell_type_names=cell_type_names, cfg=cfg)
    print(f"  flow-matching model trained: {gen.trained}")

    results = {}
    for sec, z in targets:
        print(f"  {sec}: flow-generating virtual slice at z={z:.2f}...")
        try:
            vs = gen.generate_virtual_slice(z=z)
        except Exception as e:
            print(f"    ERROR: {e}")
            import traceback; traceback.print_exc()
            continue
        n = vs.coords.shape[0]
        if n == 0:
            continue
        print(f"    -> {n} cells synthesized")
        cell_type = vs.cell_type.astype(str) if vs.cell_type is not None else np.array(["NA"] * n)
        results[sec] = {"X": sp.csr_matrix(_to_dense_f32(vs.expression)),
                        "coords": vs.coords.astype(np.float64), "cell_type": cell_type}
    return results


def main():
    parser = argparse.ArgumentParser(description="SpatialCPA-v14 wrapper (benchmark-pbya-v2)")
    _v2_io.add_v2_args(parser)
    parser.add_argument("--epochs", type=int, default=160, help="Phase B flow-matching epochs")
    parser.add_argument("--pretrain-epochs", type=int, default=60, help="Phase A encoder epochs")
    parser.add_argument("--device", default="auto", choices=["auto", "cpu", "cuda"])
    parser.add_argument("--latent-dim", type=int, default=32, help="expression latent dim d_e")
    parser.add_argument("--joint-dim", type=int, default=48, help="joint latent dim d")
    parser.add_argument("--ode-steps", type=int, default=12, help="Euler steps for the sampling ODE")
    parser.add_argument("--ensemble", type=int, default=4, help="initial noises marginalized per query")
    parser.add_argument("--position-mode", default="morph", choices=["morph", "flanking", "nearest"])
    parser.add_argument("--displacement-scale", type=float, default=0.5,
                        help="scale on the flow-decoded displacement field (morph mode)")
    parser.add_argument("--ground-blend-flow", type=float, default=0.20,
                        help="fraction of cells re-grounded to the flow-latent pick")
    parser.add_argument("--edit-weight", type=float, default=0.25,
                        help="blend toward the flow-decoded profile (0 = pure real exemplar)")
    parser.add_argument("--no-ground", action="store_true", help="disable real-profile grounding")
    parser.add_argument("--no-bio", action="store_true", help="ablate biology-informed losses")
    parser.add_argument("--no-attention", action="store_true", help="ablate 3D attention (local-only)")
    parser.add_argument("--no-output-counts", action="store_true",
                        help="emit log1p-normalized (not count-like) expression")
    parser.add_argument("--no-composition-match", action="store_true",
                        help="disable cell-type composition matching")
    args = parser.parse_args()

    if not check_environment():
        sys.exit(1)

    targets = _v2_io.load_targets(args)
    target_sections = [s for s, _ in targets]
    print(f"Loading training-only input {args.input}...")
    adata = ad.read_h5ad(args.input)
    _v2_io.guard_no_holdout(adata, target_sections)
    gene_names = adata.var_names.tolist()
    _normalize_expression(adata)

    print(f"Running SpatialCPA-v14 for targets {[(s, round(z, 2)) for s, z in targets]}...")
    t0 = time.time()
    results = run_method(adata, targets, gene_names, args)
    wall_time = time.time() - t0

    method_params = {
        "seed": args.seed, "epochs": args.epochs, "pretrain_epochs": args.pretrain_epochs,
        "latent_dim": args.latent_dim, "joint_dim": args.joint_dim,
        "ode_steps": args.ode_steps, "ensemble": args.ensemble,
        "position_mode": args.position_mode, "flow_matching": True, "generation_only": True,
    }
    _v2_io.write_prediction_h5(results, gene_names, target_sections,
                               method_params, wall_time, args.output,
                               method_name="spatialcpav14_gen")


if __name__ == "__main__":
    main()
